package Lec48;

public class Construct_Binary_Tree_from_preOrderandPostpoder {
    public class TreeNode {
     int val;
      TreeNode left;
      TreeNode right;
     TreeNode() {}
      TreeNode(int val) { this.val = val; }
      TreeNode(int val, TreeNode left, TreeNode right) {
          this.val = val;
          this.left = left;
          this.right = right;
      }
  }

}
